// Crear una función que me importa los números del 1 a n.

//Definir la función 
function imprimirNumeros(n) {
    // Inicializar el bucle que recorra del 1 hasta n
    for (let i = 1; i <= n; i++){
        // Imprimir el valor actual del i en cada iteración
        console.log(i);
    }
}

imprimirNumeros(20);